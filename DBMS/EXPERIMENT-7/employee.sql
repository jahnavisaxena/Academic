    EMPNO INT PRIMARY KEY,
    ENAME VARCHAR(50),
    JOB VARCHAR(50),
    MGR INT,
    HIREDATE DATE,
    SAL NUMBER,
    COMM NUMBER,
    DEPTNO INT
);

