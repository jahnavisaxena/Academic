SELECT AVG(SAL) AS Average_Salary FROM EMP;

SELECT COUNT(*) AS Number_of_Employees
FROM EMP;

SELECT COUNT(DISTINCT EMPNO) AS Distinct_Employees
FROM EMP;

SELECT JOB, SUM(SAL) AS Total_Salary
FROM EMP
GROUP BY JOB;

SELECT *
FROM EMP
WHERE SAL = (SELECT MAX(SAL) FROM EMP);

SELECT *
FROM EMP
WHERE DEPTNO = 10
AND SAL = (SELECT MAX(SAL) FROM EMP WHERE DEPTNO = 10);

SELECT *
FROM EMP
WHERE SAL = (SELECT (MAX(SAL) + MIN(SAL)) / 2 FROM EMP);

SELECT HIREDATE, COUNT(*) AS Employees_Joined
FROM EMP
GROUP BY HIREDATE
HAVING COUNT(*) > 1;

