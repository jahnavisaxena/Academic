CREATE SEQUENCE EMPID_SEQ
    START WITH 100
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

SELECT EMPID_SEQ.NEXTVAL FROM dual;

SELECT EMPID_SEQ.CURRVAL FROM dual;

ALTER SEQUENCE EMPID_SEQ
    CACHE 20
    MAXVALUE 1000;

INSERT INTO EMPLOYEES (Employee_id, First_Name, Last_Name, DOB, Salary, Department_id)
VALUES (EMPID_SEQ.NEXTVAL, 'John', 'Doe', TO_DATE('10-07-1985', 'DD-MM-YYYY'), 50000, 'D01');

DROP SEQUENCE EMPID_SEQ;

