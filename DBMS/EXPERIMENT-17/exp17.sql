CREATE OR REPLACE TRIGGER SALARY_VIOLATION
BEFORE INSERT OR UPDATE OF SALARY, SUPER_SSN ON EMPLOYEES1
FOR EACH ROW
BEGIN
   IF :NEW.SALARY > 100000 THEN
      RAISE_APPLICATION_ERROR(-20001, 'Salary exceeds allowed limit.');
   END IF;
   IF :NEW.SUPER_SSN = :NEW.SSN THEN
      RAISE_APPLICATION_ERROR(-20002, 'Employee cannot be their own supervisor.');
   END IF;
END;
/
